#include "ft_math.h"

t_vec2	znc(t_vec2 z, t_vec2 c, int exp)
{
	t_vec2	m;

	(void)exp;
	m.r = (z.r * z.r - z.i * z.i) + c.r;
	m.i = (2 * z.r * z.i) + c.i;
	return (m);
}
